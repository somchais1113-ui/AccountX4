create policy profiles_read_self_or_colleague on public.profiles for select to authenticated
using (
  id = (select auth.uid()) or exists (
    select 1 from public.company_members mine
    join public.company_members theirs on theirs.company_id = mine.company_id
    where mine.user_id = (select auth.uid()) and theirs.user_id = profiles.id
  )
);
create policy profiles_update_self on public.profiles for update to authenticated
using (id = (select auth.uid())) with check (id = (select auth.uid()));

create policy companies_read_member on public.companies for select to authenticated
using (public.is_company_member(id));
create policy companies_create_authenticated on public.companies for insert to authenticated
with check (created_by = (select auth.uid()));
create policy companies_update_admin on public.companies for update to authenticated
using (public.has_company_role(id, array['owner','admin']::public.company_role[]))
with check (public.has_company_role(id, array['owner','admin']::public.company_role[]));
create policy companies_delete_owner on public.companies for delete to authenticated
using (public.has_company_role(id, array['owner']::public.company_role[]));

create policy members_read_company on public.company_members for select to authenticated
using (public.is_company_member(company_id));

create policy records_read_member on public.financial_records for select to authenticated
using (public.is_company_member(company_id));
create policy records_create_editor on public.financial_records for insert to authenticated
with check (public.has_company_role(company_id, array['owner','admin','editor']::public.company_role[]));
create policy records_update_editor on public.financial_records for update to authenticated
using (public.has_company_role(company_id, array['owner','admin','editor']::public.company_role[]))
with check (public.has_company_role(company_id, array['owner','admin','editor']::public.company_role[]));
create policy records_delete_admin on public.financial_records for delete to authenticated
using (public.has_company_role(company_id, array['owner','admin']::public.company_role[]));

create policy rates_read_authenticated on public.exchange_rates for select to authenticated using (true);
create policy rates_write_admin on public.exchange_rates for all to authenticated
using (exists (
  select 1 from public.company_members
  where user_id = (select auth.uid()) and role in ('owner', 'admin')
))
with check (exists (
  select 1 from public.company_members
  where user_id = (select auth.uid()) and role in ('owner', 'admin')
));

create policy audit_read_member on public.audit_log for select to authenticated
using (company_id is null or public.is_company_member(company_id));

